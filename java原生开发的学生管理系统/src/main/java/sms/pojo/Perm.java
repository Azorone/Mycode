package sms.pojo;

public class Perm {
    private int per;
    private int id;

    public Perm(int per, int id) {
        this.per = per;
        this.id = id;
    }

    public int getPer() {
        return per;
    }

    public void setPer(int per) {
        this.per = per;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
