package sms.pojo;

public class Clst {
   private int sid;
   private int cid;

    public Clst(int sid, int cid) {
        this.sid = sid;
        this.cid = cid;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }
}
